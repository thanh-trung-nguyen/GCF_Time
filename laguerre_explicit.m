function f = laguerre_explicit(n,x,x0)

% calculate the Laguerre's function using explicit formulae given by Maple.
% Maximum dimension is 10.

if nargin < 3
    x0 = 0;
end
x = x - x0;

switch n
    case 0
        f = 1;
    case 1
        f = 1-x;
    case 2
        f = 1 + (-2 + 1/2*x).*x;
    case 3
        f = 1 + (-3 + (3/2 - 1/6*x).*x).*x;
    case 4
        f = 1 + (-4 + (3 + (-2/3 + 1/24*x).*x).*x).*x;
    case 5
        f = 1 + (-5 + (5 + (-5/3 + (5/24 - 1/120*x).*x).*x).*x).*x;
    case 6
        f = 1 + (-6 + (15/2 + (-10/3 + (5/8 + (-1/20 + 1/720*x).*x).*x).*x).*x).*x;
    case 7
        f = 1 + (-7 + (21/2 +(-35/6 + (35/24 +(-7/40 + (7/720 - 1/5040*x).*x).*x).*x).*x).*x).*x;
    case 8
        f = 1 + (-8 + (14 + (-28/3 + (35/12 + (-7/15 + (7/180 + (-1/630 + 1/40320*x).*x).*x).*x).*x).*x).*x).*x;
    case 9
        f = 1 + (-9 + (18 + (-14 + (21/4 + (-21/20 + (7/60 +(-1/140 + (1/4480 - 1/362880*x).*x).*x).*x).*x).*x).*x).*x).*x; 
    case 10
        f = 1-20*x.^3-10*x+(45/2)*x.^2-(1/36288)*x.^9-(21/10)*x.^5+(1/3628800)*x.^10+(35/4)*x.^4-(1/42)*x.^7+(7/24)*x.^6+(1/896)*x.^8;
    case 11
        f = 1-11*x+(55/2)*x.^2-(55/2)*x.^3+(55/4)*x.^4-(77/20)*x.^5+(77/120)*x.^6-(11/168)*x.^7+(11/2688)*x.^8-(11/72576)*x.^9+(11/3628800)*x.^10-(1/39916800)*x.^11;
    case 12
        f = 1-12*x+33*x.^2-(110/3)*x.^3+(165/8)*x.^4-(33/5)*x.^5+(77/60)*x.^6-(11/70)*x.^7+(11/896)*x.^8-(11/18144)*x.^9+(11/604800)*x.^10-(1/3326400)*x.^11+(1/479001600)*x.^12;
    case 13
        f = 1-13*x+39*x.^2-(143/3)*x.^3+(715/24)*x.^4-(429/40)*x.^5+(143/60)*x.^6-(143/420)*x.^7+(143/4480)*x.^8-(143/72576)*x.^9+(143/1814400)*x.^10-(13/6652800)*x.^11+(13/479001600)*x.^12-(1/6227020800)*x.^13;
    case 14
        f =1-14*x+(91/2)*x.^2-(182/3)*x.^3+(1001/24)*x.^4-(1001/60)*x.^5+(1001/240)*x.^6-(143/210)*x.^7+(143/1920)*x.^8-(143/25920)*x.^9+(143/518400)*x.^10-(13/1425600)*x.^11+(13/68428800)*x.^12-(1/444787200)*x.^13+(1/87178291200)*x.^14;
    case 15
        f =1-15*x+(105/2)*x.^2-(455/6)*x.^3+(455/8)*x.^4-(1001/40)*x.^5+(1001/144)*x.^6-(143/112)*x.^7+(143/896)*x.^8-(143/10368)*x.^9+(143/172800)*x.^10-(13/380160)*x.^11+(13/13685760)*x.^12-(1/59304960)*x.^13+(1/5811886080)*x.^14-(1/1307674368000)*x.^15;
    
    otherwise
        error('n must be from 0 to 15');
end
f = f.*exp(-x/2);
f = f*(-1)^n; %to make all the coefficient positives